/**
 *  \file CardTypes.h
 *  \brief File implementing the 4 possible suits and 13 possible values a playing card can have
 */

#ifndef CARDTYPES_H
#define CARDTYPES_H

/**
 *  \brief A type Suit representing the possible suits of a card
 */
enum Suit {hearts, diamonds, spades, clubs};
/**
 *  \brief A type Value representing the possible values of a card
 */
enum Value {ace, two, three, four, five, six, seven, eight, nine, ten, jack, queen, king};

#endif